    private void simpanData() {
        if(validasiInput()==true){
            String id_distributor     = t_iddistributor.getText();
            String nama_distributor   = t_namadistributor.getText();
            String telp_distributor   = t_telpdistributor.getText();
            String alamat_distributor = t_alamatdistributor.getText();

            Model_Distributor dst = new Model_Distributor();
            dst.setId_distributor       (id_distributor);
            dst.setNama_distributor     (nama_distributor);
            dst.setTelp_distributor     (telp_distributor);
            dst.setAlamat_distributor   (alamat_distributor);

            servis.tambahData(dst);
            tblModel.tambahData(dst);
            bt_tambahdata1.setText("TAMBAH");
            loadData();
            resetForm();
            tampilPanel();
        }    
    }
    
    private boolean validasiInput() {
        boolean valid = false;
        if (t_iddistributor.getText().trim().isEmpty() ){
            JOptionPane.showMessageDialog(null, "ID Distributor tidak boleh kosong");
        }else if (t_namadistributor.getText().trim().isEmpty() ){
            JOptionPane.showMessageDialog(null, "Nama Distributor tidak boleh kosong");
        }else if (t_telpdistributor.getText().trim().isEmpty() ){
            JOptionPane.showMessageDialog(null, "No Telepon tidak boleh kosong");
        }else if (t_telpdistributor.getText().length()<10 ){
            JOptionPane.showMessageDialog(null, "No Telepon terlalu pendek");
        }else if (t_telpdistributor.getText().length()>13 ){
            JOptionPane.showMessageDialog(null, "No Telepon terlalu panjang");
        }else if (t_alamatdistributor.getText().trim().isEmpty() ){
            JOptionPane.showMessageDialog(null, "Alamat tidak boleh kosong");
        }else{
            valid = true;
        }
        return valid;
    }

