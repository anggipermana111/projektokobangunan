    private void t_telpdistributorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_t_telpdistributorKeyReleased
        char key = (char) evt.getKeyCode();
        if (key >= '0' && key <= '9' || key == '\b' || key=='\n') {
            kata = t_telpdistributor.getText();
        } else {
            t_telpdistributor.setText(kata);
        }
    }//GEN-LAST:event_t_telpdistributorKeyReleased

