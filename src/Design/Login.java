    private void prosesLogin() {
        if (validasiInput() == true) {
            String id = t_username.getText();
            String user = t_username.getText();
            String pass = t_password.getText();

            Model_Login log = new Model_Login();

            log.setId_user(id);
            log.setUsername(user);
            log.setPass_user(pass);

            if (servis.prosesLogin(log)) {
                dispose();
            } else {
                resetLogin();
            }

        }
    }

    private void resetLogin() {
        t_username.setText("");
        t_password.setText("Masukkan Password...");
        t_password.setForeground(Color.GRAY);
        t_password.setEchoChar((char) 0);
        t_username.requestFocus();
    }

    private boolean validasiInput() {
        boolean valid = false;
        if (t_username.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Username tidak boleh kosong");
            resetLogin();
        } else if (t_password.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "Password tidak boleh kosong");
            resetLogin();
        } else {
            valid = true;
        }
        return valid;
    }

    private void cekIsi() {
        if (t_username.getForeground().toString().equals("java.awt.Color[r=128,g=128,b=128]")) {
            t_username.setText("");
        }
        if (t_password.getForeground().toString().equals("java.awt.Color[r=128,g=128,b=128]")) {
            t_password.setText("");
        }
    }

    private void b_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_loginActionPerformed
        cekIsi();
        prosesLogin();
    }//GEN-LAST:event_b_loginActionPerformed

    private void t_usernameFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_t_usernameFocusGained
        if (t_username.getForeground().toString().equals("java.awt.Color[r=128,g=128,b=128]")) {
            t_username.setForeground(Color.BLACK);
            t_username.setText("");
        }
    }//GEN-LAST:event_t_usernameFocusGained

    private void t_usernameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_t_usernameFocusLost
        if (t_username.getText().equals("")) {
            t_username.setForeground(Color.GRAY);
            t_username.setText("Masukan username...");
        }
    }//GEN-LAST:event_t_usernameFocusLost

    private void t_passwordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_t_passwordFocusLost
        if (t_password.getPassword().length == 0) {
            t_password.setEchoChar('\u0000');
            t_password.setForeground(Color.GRAY);
            t_password.setText("Masukkan Password...");
        }
    }//GEN-LAST:event_t_passwordFocusLost

