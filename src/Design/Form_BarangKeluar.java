    private void t_jumbarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_t_jumbarActionPerformed
        if (t_jumbar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Jumlah masih kosong!");
        } else if (t_jumbar.getText().toString().equals("0")) {
            JOptionPane.showMessageDialog(null, "Jumlah tidak boleh 0!");
        } else {
            String kode = t_kodbar.getText();
            String nama = t_nambar.getText();
            String satuan = t_satuan.getText();
            
            bt_perbaruisem.setEnabled(false);
            
            
            Long harga = Long.valueOf(t_harbar.getText());
            int jumlah = Integer.parseInt(t_jumbar.getText());
            Long subtotal = harga * jumlah;
            t_subtotal.setText(Long.toString(subtotal));

            Model_Barang brg = new Model_Barang();
            Model_DetBarangKeluar det_keluar = new Model_DetBarangKeluar();
//        Model_BarangKeluar keluar = new Model_BarangKeluar();
            Model_SemBarangKeluar smt = new Model_SemBarangKeluar();

            brg.setKode_barang(kode);
            brg.setNama_barang(nama);
            brg.setSatuan(satuan);
            brg.setHarga(harga);
            det_keluar.setMdl_barang(brg);
            det_keluar.setJml_keluar(jumlah);
            det_keluar.setSubtotal_keluar(subtotal);

            smt.setMdl_barang(brg);
            smt.setMdl_detkeluar(det_keluar);

            if (servis_detkeluar.validasiStok(det_keluar)) {

                //sumSubTotal();
                if (JOptionPane.showConfirmDialog(this, "Mau Tambah Barang?",
                        "Konfirmasi", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    servis.tambahData(smt);
                    servis_detkeluar.sumTotal(det_keluar);
                    lb_tobar.setText(det_keluar.getSubtotal_keluar().toString());

                    loadDataSementara();
                    aktif();
                    bersihBarang();
                    t_kodbar.requestFocus();

                } else {
                    bersihBarang();
                    //t_bayar.setEditable(true);
                    //t_bayar.requestFocus();
                }
            } else {
                t_jumbar.requestFocus();
            }
        }
    }//GEN-LAST:event_t_jumbarActionPerformed

