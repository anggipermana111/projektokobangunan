    private void t_jumbarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_t_jumbarKeyReleased
        char key = (char) evt.getKeyCode();
        if (key >= '0' && key <= '9' || key == '\b' || key == '\n') {
            Integer a = 0, b = 0;
            if (!(t_jumbar.getText().isEmpty())) {
                a = Integer.parseInt(t_harbar.getText());
                b = Integer.parseInt(t_jumbar.getText());
            }
            Integer x = a * b;
            t_subtotal.setText(x.toString());
            kata = t_jumbar.getText();
        } else {
            t_jumbar.setText(kata);
        }
    }//GEN-LAST:event_t_jumbarKeyReleased

