    private void perbaruiData() {
        int index = tbl_sementara.getSelectedRow();
        if (index != -1) {
            Model_SemBarangMasuk modsem = tblModel.getData(tbl_sementara.convertRowIndexToModel(index));

            String kode_barang = t_kodbar.getText();
            String nama_barang = t_nambar.getText();
            String satuan = t_satuan.getText();
            Long harga = Long.valueOf(t_harbar.getText());
            Integer jumlah = Integer.parseInt(t_jumbar.getText());
            Long subtotal = Long.valueOf(t_subtotal.getText());

            Model_SemBarangMasuk smt = new Model_SemBarangMasuk();
            Model_Barang brg = new Model_Barang();
            Model_DetBarangMasuk det_masuk = new Model_DetBarangMasuk();

            brg.setKode_barang(kode_barang);
            brg.setNama_barang(nama_barang);
            brg.setSatuan(satuan);
            brg.setHarga(harga);
            det_masuk.setJml_masuk(jumlah);
            det_masuk.setSubtotal_masuk(subtotal);

            smt.setMdl_barang(brg);
            smt.setMdl_detmasuk(det_masuk);

            Long total = Long.valueOf(lb_tobar.getText());

            Long a = Long.valueOf(tbl_sementara.getModel().getValueAt(index, 4).toString());
            Long b = Long.valueOf(tbl_sementara.getModel().getValueAt(index, 3).toString());

            total -= a * b;
            total += harga * Long.valueOf(jumlah.toString());
            lb_tobar.setText(total.toString());

            servis.perbaruiData(smt);
            tblModel.perbaruiData(index, smt);
            bersihBarang();
//            loadDataSementara();
            bt_perbaruisem.setEnabled(false);
            bt_hapusem.setEnabled(false);

        } else {
            JOptionPane.showMessageDialog(null, "Pilih dahulu record yang akan diperbarui");
        }

    }

