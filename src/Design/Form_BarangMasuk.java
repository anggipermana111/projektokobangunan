    private void simpanData() {
        if (lb_tobar.getText().toString().equals("0")) {
            JOptionPane.showMessageDialog(null, "Data Kosong!");
        } else {
            String no_masuk = t_no.getText();
            String tgl_masuk = t_tanggal.getText();
            String kode = t_kodbar.getText();
            String nama = t_nambar.getText();
            String id_dis = t_iddis.getText();
            String id_peng = lb_id.getText();
            String status = "Sedang di masuk";

//        int jumlah      = Integer.parseInt(t_jumbar.getText());
//        Long subtotal   = Long.parseLong(t_subtotal.getText());
            Long total = Long.valueOf(lb_tobar.getText());
            Model_Barang brg = new Model_Barang();
            Model_Distributor dst = new Model_Distributor();
            Model_Pengguna pgn = new Model_Pengguna();
            Model_DetBarangMasuk det_masuk = new Model_DetBarangMasuk();
            Model_BarangMasuk masuk = new Model_BarangMasuk();

            // Simpan Tabel BarangMasuk
            masuk.setNo_masuk(no_masuk);
            masuk.setTgl_masuk(tgl_masuk);
            masuk.setTotal_masuk(total);
            dst.setId_distributor(id_dis);
            pgn.setId_pengguna(id_peng);

            masuk.setMdl_dis(dst);
            masuk.setMdl_peng(pgn);

            det_masuk.setMdl_masuk(masuk);
            det_masuk.setMdl_barang(brg);

            servis_masuk.tambahData(masuk);
            servis_detmasuk.tambahData(det_masuk);
            servis_detmasuk.hapusSementara(det_masuk);
            tblModel_masuk.tambahData(masuk);

        }

    }

